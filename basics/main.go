package main

import "fmt"

func main() {
	fmt.Println("hello Gopher!!!")

	fmt.Printf("hello %s!!!!", "Gopher")

	fmt.Println("next line")

	fmt.Printf("hello %d!!!!\n", 66)

	fmt.Printf("hello %v!!!!\n", "Gopher")
	fmt.Printf("hello %v!!!!\n", 77)

	fmt.Printf("hello %v : %v !! \n", "Gopher", 66)
}
